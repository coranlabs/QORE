package pfcpType

import (
	"encoding/binary"
	"fmt"
)

type QuotaValidityTime struct {
	QuotaValidityTime uint32
}

func (q *QuotaValidityTime) MarshalBinary() (data []byte, err error) {
	var idx uint16 = 0
	// Octet 5 to 8
	data = make([]byte, 4)
	binary.BigEndian.PutUint32(data[idx:], q.QuotaValidityTime)

	return data, nil
}

func (q *QuotaValidityTime) UnmarshalBinary(data []byte) error {
	length := uint16(len(data))

	var idx uint16 = 0
	// Octet 5 to 8
	if length < idx+4 {
		return fmt.Errorf("Inadequate TLV length: %d", length)
	}
	q.QuotaValidityTime = binary.BigEndian.Uint32(data[idx:])
	idx = idx + 4

	if length != idx {
		return fmt.Errorf("Inadequate TLV length: %d", length)
	}

	return nil
}
